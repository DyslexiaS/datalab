/*
 * remainderPower2 - Compute x%(2^n), for 0 <= n <= 30
 *                   Negative arguments should yield negative remainders
 *   Examples: remainderPower2(15, 2) = 3, remainderPower2(-35, 3) = -3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int remainderPower2(int x, int n)
{
    int is_neg = x >> 31;
    int quotient = (x + (((1 << n) + ~0) & (is_neg & 1))) >> n;
    int no_rem = quotient << n;
    int t_min = 1 << 31;
    int x_eq_tmin = !(x ^ t_min);
    // printf("%d\n", no_rem);
    // printf("%x\n", x - no_rem);
    return (x_eq_tmin - 1) & (x + ~no_rem + 1);
}

/*
 * replaceByte(x,n,c) - Replace byte n in x with c
 *                      Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678, 1, 0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c)
{
    int shift_dis = n << 3;
    int clear_mask = ~(0xFF << shift_dis);
    int clear_x = x & clear_mask;
    int replace = c << shift_dis;
    return clear_x | replace;
}

/*
 * rotateLeft - Rotate x to the left by n
 *              Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321, 4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3
 */
int rotateLeft(int x, int n)
{
    int shift_dis = 32 + ~n + 1;
    int mask = (1 << n) + ~0;
    int save = mask & (x >> shift_dis);
    return (x << n) | save;
}

/*
 * rotateRight - Rotate x to the right by n
 *               Can assume that 0 <= n <= 31
 *   Examples: rotateRight(0x87654321, 4) = 0x18765432
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3
 */
int rotateRight(int x, int n)
{
    int shift_dis = 32 + ~n + 1;
    int mask = (1 << n) + ~0;
    int save = (mask & x) << shift_dis;
    int clear = mask << shift_dis;
    return ((x >> n) & ~clear) | save;
}

/*
 * satAdd - adds two numbers but when positive overflow occurs, returns
 *          maximum possible value, and when negative overflow occurs,
 *          it returns minimum positive value.
 *   Examples: satAdd(0x40000000, 0x40000000) = 0x7fffffff
 *             satAdd(0x80000000, 0xffffffff) = 0x80000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 30
 *   Rating: 4
 */
int satAdd(int x, int y)
{
    int sum = x + y;
    int overflow = ((sum ^ x) & (sum ^ y)) >> 31;  // -1 or 0
    int is_xy_pos = sum >> 31;                     // -1 or 0
    return (sum & ~overflow) | (overflow & ((1 << 31) ^ is_xy_pos));
}

/*
 * satMul2 - multiplies by 2, saturating to Tmin or Tmax if overflow
 *   Examples: satMul2(0x30000000) = 0x60000000
 *             satMul2(0x40000000) = 0x7FFFFFFF (saturate to TMax)
 *             satMul2(0x80000001) = 0x80000000 (saturate to TMin)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int satMul2(int x)
{
    int mul2 = x << 1;
    int is_x_pos = mul2 >> 31;        // -1 or 0
    int overflow = (mul2 ^ x) >> 31;  // -1 or 0
    return (mul2 & ~overflow) | (overflow & ((1 << 31) ^ is_x_pos));
}

/*
 * satMul3 - multiplies by 3, saturating to Tmin or Tmax if overflow
 *   Examples: satMul3(0x10000000) = 0x30000000
 *             satMul3(0x30000000) = 0x7FFFFFFF (Saturate to TMax)
 *             satMul3(0x70000000) = 0x7FFFFFFF (Saturate to TMax)
 *	       satMul3(0xD0000000) = 0x80000000 (Saturate to TMin)
 *             satMul3(0xA0000000) = 0x80000000 (Saturate to TMin)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3
 */
int satMul3(int x)
{
    int mul2 = x << 1;  // case of mul2 overflow
    int mul3 = mul2 + x;
    int overflow = ((mul2 ^ x) | (mul3 ^ x)) >> 31;
    int tmax = ~(1 << 31);  // handle the max case
    int sign_x = x >> 31;   // -1 or 0
    return (overflow & (tmax ^ sign_x)) | (~overflow & mul3);
}

